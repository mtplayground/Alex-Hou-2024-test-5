export function normalizeRotation(rotation: number): RotationIndex {
  const normalized = ((rotation % 4) + 4) % 4

  return normalized as RotationIndex
}

export function getTetrominoMatrix(
  type: TetrominoType,
  rotation: number
): TetrominoMatrix {
  return TETROMINO_ROTATIONS[type][normalizeRotation(rotation)]
}

export function getTetrominoCells(
  type: TetrominoType,
  rotation: number
): Point[] {
  const matrix = getTetrominoMatrix(type, rotation)
  const cells: Point[] = []

  for (let y = 0; y < matrix.length; y += 1) {
    for (let x = 0; x < matrix[y].length; x += 1) {
      if (matrix[y][x] === 1) {
        cells.push({ x, y })
      }
    }
  }

  return cells
}

